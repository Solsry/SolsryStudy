public class Operator {
    public static void main(String[] args) {
        int kor, eng; // 변수 선언 -> 기억 공간이 만들어진다는 뜻.
        kor = 77 ;
        eng = 87 ;

        int total;
        total = kor + eng;

        System.out.println("총점:"+total);
        System.out.println("평균:"+(total/2));
    }
}
