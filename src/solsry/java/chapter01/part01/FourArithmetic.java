package solsry.java.chapter01.part01;

public class FourArithmetic {
    public static void main(String[] args) {
        int a, b;
        a = 12;
        b = 2;

        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
    }
}
