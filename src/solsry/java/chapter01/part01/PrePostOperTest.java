package solsry.java.chapter01.part01;

public class PrePostOperTest {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(--x);
        System.out.println(x++);
        System.out.println(x--);
        System.out.println(++x);
        System.out.println(x);
    }
}
